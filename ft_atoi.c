#include "libft.h"

int	ft_atoi(const char *str)
{
	char	*s;
	int		sign;
	int		res;
	int		i;

	sign = 1;
	res = 0;
	i = 0;
	s = (char *)str;
	while ((s[i] == '\t') || (s[i] == '\n')
		|| (s[i] == '\v') || (s[i] == '\f')
		|| (s[i] == '\r') || (s[i] == ' '))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign *= -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + (s[i] - '0');
		i++;
	}
	return (sign * res);
}
